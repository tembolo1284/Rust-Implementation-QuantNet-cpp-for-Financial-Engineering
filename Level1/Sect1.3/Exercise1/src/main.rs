// Exercise 1 Requirements:
// ------------------------
// printf() is a standard function. Each compiler will support this function.
// Between the () is stated what has to be printed (on screen).
// Create a program that prints the following when executed:
//
// My first C-program
// is a fact!
// Good, isn't it?
//
// Note: In Rust, we use println!() macro instead of printf()

fn main() {
    println!("My first C-program");
    println!("is a fact!");
    println!("Good, isn't it?");
}
